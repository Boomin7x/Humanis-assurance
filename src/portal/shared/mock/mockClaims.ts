// Helper functions
export const getClaimById = (id: string): Claim | undefined => {
  return mockClaims.find((claim) => claim.id === id);
};

export const getClaimsByCustomerId = (customerId: string): Claim[] => {
  return mockClaims.filter((claim) => claim.customerId === customerId);
};

export const getClaimsByPolicyId = (policyId: string): Claim[] => {
  return mockClaims.filter((claim) => claim.policyId === policyId);
};

export const getClaimsByStatus = (status: ClaimStatus): Claim[] => {
  return mockClaims.filter((claim) => claim.status === status);
};
