// Helper functions
export const getPolicyById = (id: string): Policy | undefined => {
  return mockPolicies.find((policy) => policy.id === id);
};

export const getPoliciesByCustomerId = (customerId: string): Policy[] => {
  return mockPolicies.filter((policy) => policy.customerId === customerId);
};

export const getPoliciesByAgentId = (agentId: string): Policy[] => {
  return mockPolicies.filter((policy) => policy.agentId === agentId);
};

export const getPoliciesByStatus = (status: PolicyStatus): Policy[] => {
  return mockPolicies.filter((policy) => policy.status === status);
};

export const getExpiringPolicies = (days: number = 30): Policy[] => {
  const now = new Date();
  const targetDate = new Date();
  targetDate.setDate(now.getDate() + days);

  return mockPolicies.filter((policy) => {
    return (
      policy.status === "Active" &&
      policy.endDate >= now &&
      policy.endDate <= targetDate
    );
  });
};
